"use client";

import { notifierVIInterface } from "@/lib/implementations/models/notifier";
import { signInVIInterface } from "@/lib/implementations/models/sign-in";
import { useInitializedStatefulInteractiveModel } from "@mvc-react/stateful";
import { useSearchParams } from "next/navigation";
import { SignInNotificationType } from "./sign-in-model";
import Spinner from "react-bootstrap/Spinner";

const SignIn = () => {
	const endpoint = useSearchParams().get("endpoint") ?? "";
	const notifier = useInitializedStatefulInteractiveModel(
		notifierVIInterface<SignInNotificationType>(),
		{ notification: null },
	);
	const { interact } = useInitializedStatefulInteractiveModel(
		signInVIInterface(endpoint),
		{
			notifier,
		},
	);
	const { notification } = notifier.modelView;

	return (
		<main className="w-full grow flex items-center">
			<div className="flex w-full justify-center p-8 md:p-12">
				<div className="w-full max-w-[400px] h-full flex items-center bg-white/90 shadow rounded-3xl p-12 md:w-[400px] md:p-14">
					<div className="flex flex-col grow gap-6 justify-center items-center md:flex-nowrap">
						<span className="text-3xl">Sign In</span>
						{notification?.type == "failure" && (
							<span className="text-red-800 text-center">
								{notification.text}
							</span>
						)}
						{notification?.type == "success" && (
							<span className="text-center">
								{notification.text}
							</span>
						)}
						<button
							className="flex w-full p-4 py-3 items-center justify-center gap-2 rounded-lg! text-white bg-gray-800 disabled:bg-gray-400 hover:bg-gray-900"
							disabled={notification?.type == "pending"}
							onClick={() => {
								interact({
									type: "GOOGLE",
									input: { notifier },
								});
							}}
						>
							{notification?.type == "pending" ? (
								<Spinner
									animation="border"
									color="white"
									className="size-6!"
								/>
							) : (
								<>
									<svg
										xmlns="http://www.w3.org/2000/svg"
										width="1em"
										height="1em"
										viewBox="0 0 256 262"
									>
										<path
											fill="#4285F4"
											d="M255.878 133.451c0-10.734-.871-18.567-2.756-26.69H130.55v48.448h71.947c-1.45 12.04-9.283 30.172-26.69 42.356l-.244 1.622l38.755 30.023l2.685.268c24.659-22.774 38.875-56.282 38.875-96.027"
										></path>
										<path
											fill="#34A853"
											d="M130.55 261.1c35.248 0 64.839-11.605 86.453-31.622l-41.196-31.913c-11.024 7.688-25.82 13.055-45.257 13.055c-34.523 0-63.824-22.773-74.269-54.25l-1.531.13l-40.298 31.187l-.527 1.465C35.393 231.798 79.49 261.1 130.55 261.1"
										></path>
										<path
											fill="#FBBC05"
											d="M56.281 156.37c-2.756-8.123-4.351-16.827-4.351-25.82c0-8.994 1.595-17.697 4.206-25.82l-.073-1.73L15.26 71.312l-1.335.635C5.077 89.644 0 109.517 0 130.55s5.077 40.905 13.925 58.602z"
										></path>
										<path
											fill="#EB4335"
											d="M130.55 50.479c24.514 0 41.05 10.589 50.479 19.438l36.844-35.974C195.245 12.91 165.798 0 130.55 0C79.49 0 35.393 29.301 13.925 71.947l42.211 32.783c10.59-31.477 39.891-54.251 74.414-54.251"
										></path>
									</svg>
									Sign in with Google
								</>
							)}
						</button>
					</div>
				</div>
			</div>
		</main>
	);
};

export default SignIn;
